import pkgutil

# Workaround for python import being overly prescriptive
# This will get used only if you do not add to path
# Simply propagates the lower namespace into the upper one
# That way our FPGA projects can import this without having to set anything up
__all__ = []
for loader, module_name, is_pkg in pkgutil.walk_packages(__path__):
    if "utils" in module_name:
        continue
    __all__.append(module_name)
    _module = loader.find_module(module_name).load_module(module_name)
    globals()[module_name] = _module
